    public void Conferencias() throws SQLException{
        micombo = new DefaultComboBoxModel();
        t = c.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
        String sql = "Select Titulo from conferencias";
        r = t.executeQuery(sql);
        r.beforeFirst();
        while(r.next()){
            String dato = r.getString(1);
            micombo.addElement(String.valueOf(dato));
        }
        this.jComboBox1.setModel(micombo);
    }
    public void iniciarTabla(){
        mitabla = new DefaultTableModel();
        mitabla.addColumn("matricula");
        mitabla.addColumn("nombre");
        mitabla.addColumn("apellido1");
        mitabla.addColumn("apellido2");
        mitabla.addColumn("fecha");
        mitabla.addColumn("hora");
        this.jTable1.setModel(mitabla);
    }
    public void llenarTabla() throws SQLException{
        t = null;
        t = c.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
        String sql = "SELECT alumnos.matricula, alumnos.nombre, alumnos.apellido1, alumnos.apellido2, "
           + "registro.fecha, registro.hora "
           + "FROM alumnos "
           + "JOIN registro ON alumnos.matricula = registro.matricula_alumnos "
           + "JOIN conferencias ON conferencias.idConferencia = registro.idConferencia "
           + "WHERE conferencias.idConferencia = "
           + Integer.parseInt(this.jLabel12.getText());
        System.out.println(sql);
        r = t.executeQuery(sql);
        r.beforeFirst();
        while(r.next()){
            mitabla = (DefaultTableModel) this.jTable1.getModel();
            Object datos[] = {r.getString(1), r.getString(2), r.getString(3), r.getString(4), r.getString(5), r.getString(6)};
            mitabla.addRow(datos);
            jTable1.setModel(mitabla);
        }
    }
    public void registro(String m) throws SQLException{
        t = null;
        t = c.createStatement();
        String sql = "Insert into registro (matricula_alumnos,idConferencia, fecha, hora)"+
                     " values('" + m + "'," +Integer.parseInt(this.jLabel12.getText())+
                     ",+date(now()),time(now())";
        t.executeUpdate(sql);
        System.out.println(sql);
        iniciarTabla();
        llenarTabla();
    }
