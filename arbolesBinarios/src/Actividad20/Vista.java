    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String clave = jTextFieldClave.getText().trim();
        if(clave.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debes de ingresar una clave");
            return;
        }
        if(gestion.buscar(clave)){
            JOptionPane.showMessageDialog(this, "Clave " +clave+
                    " encontrada");
        } 
        else {
            JOptionPane.showMessageDialog(this, "Clave " +clave+
                    " no encontrada");
        }
        jTextFieldClave.setText("");
        jTextFieldValor.setText("");
        jTextFieldClave.requestFocus();
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String clave = jTextFieldClave.getText().trim();
        String valor = jTextFieldValor.getText().trim();
        if(gestion.eliminar(clave, valor)){
            JOptionPane.showMessageDialog(this, "Elemento Eliminado");
        }
        else{
            JOptionPane.showMessageDialog(this, "La clave no existe");
        }
        jTextFieldClave.setText("");
        jTextFieldValor.setText("");
        jTextFieldClave.requestFocus();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnInsertarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnInsertarMouseClicked
        // TODO add your handling code here:
        String clave = jTextFieldClave.getText().trim();
        String valor = jTextFieldValor.getText().trim();
        if(clave.isEmpty() && valor.isEmpty()){
            JOptionPane.showMessageDialog(this, "Debes ingresar una Clave y "
                    + " un Valor");
        }
        if(gestion.agregar(clave, valor)){
            JOptionPane.showMessageDialog(this, "Elemento agregado");
        }
        else{
            JOptionPane.showMessageDialog(this,"Elemento no se agrego");
        }
        jTextFieldClave.setText("");
        jTextFieldValor.setText("");
        jTextFieldClave.requestFocus();
    }//GEN-LAST:event_btnInsertarMouseClicked

